package cubo;

public class Cubo {

	private double a;
	private double m;
	private int tinta;
	
	public Cubo(double a, double m, int tinta) {
		super();
		this.a = a;
		this.m = m;
		this.tinta = tinta;
	}

	//metodos


	public double getA() {
		return a;
	}



	public void setA(double a) {
		this.a = a;
	}



	public double getM() {
		return m;
	}



	public void setM(double m) {
		this.m = m;
	}



	public int getTinta() {
		return tinta;
	}



	public void setTinta(int tinta) {
		this.tinta = tinta;
	}
	
}
